variavel_1 = 10
variavel_2 = 20
variavel_3 = 30
print(variavel_1, variavel_2, variavel_3)

multiplicacao_2 = variavel_1 * 2

multiplicacao_3 = variavel_2 * 3

multiplicacao_5 = variavel_3 * 5
print(multiplicacao_2, multiplicacao_3, multiplicacao_5)

soma_multiplicacao = multiplicacao_2 + multiplicacao_3 + multiplicacao_5
print(soma_multiplicacao)

variavel_final = soma_multiplicacao / 10
print(variavel_final)

""" O objetivo geral foi definir três variáveis(variavel_1, variavel_2 e variavel_3),
com os valores respectivos 10, 20 e 30. Onde cada variável seria multiplicada
por pesos diferentes: o valor 10 foi multiplicado por 2 (multiplicacao_2),
o 20 foi multiplicado por 3(multiplicacao_3) e o 30 foi multiplicado
por 5(multiplicacao_5). Os resultados dessas multiplicações
foram somados (soma_multiplicacao) e, em seguida, o total foi dividido por 10,
que é a soma dos pesos, gerando o valor final (variavel_final)."""


